.device atmega328P
.nolist
.list

.ORG 0X000


    rjmp INICIO

INICIO:

	ldi r17, 0x37
	ldi r18, 45
	ldi r19, 0b00111000
	ldi r20, 0x2B
	add r17, r18
	add r19, r20
	sub r17, r19
	nop

LOOP:
	rjmp LOOP

.EXIT 